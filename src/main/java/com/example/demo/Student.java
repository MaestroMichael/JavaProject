package com.example.demo;

public class Student {

    final public long id;
    final public String name;
    final public String numer;
    final public String grupa;


    public Student(long id, String name, String numer, String grupa) {
        this.id = id;
        this.name = name;
        this.numer = numer;
        this.grupa = grupa;
    }
}
